def generate_tabulations(count = 1):
    index = 1
    tabs = ''
    
    while index <= count:
        tabs += '    '

        index += 1

    return tabs

def generate_line_break(count = 1):
    index = 1
    line_break = ''
    
    while index <= count:
        line_break += '\n'

        index += 1

    return line_break

